package drawing.canvas;

/**
 * Константы для рисования.
 *
 * ЗАЧЕМ ОТДЕЛЬНЫЙ КЛАСС?
 *
 * Альтернативы:
 * 1. Magic values в коде: pixels[y][x] = ' '
 *    Проблема: непонятно, что значит ' '. И если нужно поменять — искать везде.
 *
 * 2. Константы в Canvas: Canvas.EMPTY_CHAR
 *    Проблема: Canvas становится свалкой констант. Нарушается Single Responsibility.
 *
 * 3. Отдельный класс констант (мой выбор)
 *    - Все "магические" значения в одном месте
 *    - Легко найти и изменить
 *    - Самодокументирующийся код: EMPTY_CHAR понятнее, чем ' '
 *
 * РАЗДЕЛЕНИЕ КОНСТАНТ
 *
 * Я разделяю константы на две группы:
 *
 * 1. Символы для данных (хранятся в Canvas):
 *    - EMPTY_CHAR ' ' — пустой пиксель
 *    - LINE_CHAR 'x' — линия/контур
 *
 * 2. Символы для отображения (используются только в Renderer):
 *    - HORIZONTAL_BORDER '-'
 *    - VERTICAL_BORDER '|'
 *
 * Это подчёркивает разделение данных и представления.
 *
 * ОГРАНИЧЕНИЯ РАЗМЕРА
 *
 * MAX_CANVAS_WIDTH/HEIGHT защищают от:
 * - Переполнения памяти (1000x1000 char = 2MB, терпимо; 10000x10000 = 200MB, нет)
 * - Долгих вычислений (BucketFill на 1М пикселей — секунды)
 * - Бессмысленного вывода (1000 символов в строке терминала уже нечитаемо)
 */
public final class DrawingConstants {

    /** Пустой пиксель (пробел). */
    public static final char EMPTY_CHAR = ' ';

    /** Символ для линий и контуров. */
    public static final char LINE_CHAR = 'x';

    /** Горизонтальная рамка (только для отображения). */
    public static final char HORIZONTAL_BORDER = '-';

    /** Вертикальная рамка (только для отображения). */
    public static final char VERTICAL_BORDER = '|';

    /** Максимальная ширина холста. */
    public static final int MAX_CANVAS_WIDTH = 1000;

    /** Максимальная высота холста. */
    public static final int MAX_CANVAS_HEIGHT = 1000;

    /**
     * Приватный конструктор — класс констант нельзя инстанцировать.
     */
    private DrawingConstants() {
    }
}

package drawing.exception;

/**
 * Единственный тип исключения для ошибок приложения.
 *
 * ПОЧЕМУ ОДНО ИСКЛЮЧЕНИЕ, А НЕ ИЕРАРХИЯ?
 *
 * Можно было бы создать иерархию:
 *
 *   DrawingException
 *   ├── InvalidCommandException
 *   ├── CanvasNotCreatedException
 *   ├── OutOfBoundsException
 *   └── ...
 *
 * Но это было бы over-engineering, потому что:
 *
 * 1. ВСЕ ошибки обрабатываются ОДИНАКОВО: показываем сообщение и продолжаем.
 *    Нет случаев, когда нужно отличать тип ошибки.
 *
 * 2. Сообщение об ошибке уже содержит всю нужную информацию для пользователя.
 *    "Point (50,50) out of bounds (canvas: 20x4)" — понятно и без имени класса.
 *
 * 3. Меньше классов = меньше сложности = меньше багов.
 *
 * ПОЧЕМУ RuntimeException, А НЕ checked Exception?
 *
 * 1. Checked exceptions заставляют добавлять throws везде по цепочке.
 *    Это засоряет сигнатуры методов.
 *
 * 2. Все ошибки ловятся в одном месте — в главном цикле DrawingApp.run().
 *    Промежуточным методам не нужно знать о них.
 *
 * 3. Это ошибки пользовательского ввода, а не системные сбои.
 *    Программа не может их "исправить" — только сообщить пользователю.
 *
 * СООБЩЕНИЯ ОБ ОШИБКАХ
 *
 * Каждое сообщение должно быть:
 * - Понятным: что пошло не так
 * - Actionable: что делать дальше
 *
 * Примеры:
 * - "Canvas not created. Use: C <width> <height>" — понятно, что делать
 * - "Point (50,50) out of bounds (canvas: 20x4)" — видно, в чём проблема
 * - "Usage: L <x1> <y1> <x2> <y2>" — показываем правильный формат
 */
public final class DrawingException extends RuntimeException {

    public DrawingException(String message) {
        super(message);
    }
}
